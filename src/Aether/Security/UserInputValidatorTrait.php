namespace Aether\Security;

trait UserInputValidatorTrait {


    /*
     * @param string $_str
     *
     * @return string
     */
    public function _sanitizeInput(string $_str) : string {
        return htmlspecialchars($_str, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
    }
}